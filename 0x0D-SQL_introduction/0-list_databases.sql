-- The SHOW DATABASES command lists all the databases on the MySQL server
-- This is useful to see what databases currently exist on the server
-- It does not require any parameters

SHOW DATABASES;

-- Output:
-- The command returns a list of database names currently on the server.
-- Example output:
-- +--------------------+
-- | Database           |
-- +--------------------+
-- | information_schema |
-- | mysql              |
-- | performance_schema |
-- | SchoolDB           |
-- | sys                |
-- +--------------------+
-- Each database name represents a separate database that can be selected and used.

